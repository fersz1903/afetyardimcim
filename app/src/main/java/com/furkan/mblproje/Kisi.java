package com.furkan.mblproje;

public class Kisi {
    private String Ad;
    private String numara;


    public Kisi(String ad, String numara) {
        this.Ad = ad;
        this.numara = numara;
    }

    public String getAd() {
        return Ad;
    }

    public String getNumara() {
        return numara;
    }

}
